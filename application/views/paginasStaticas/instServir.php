        <p>O SERVIR – Serviço de Promoção da Criança e do Adolescente – é uma Entidade sem fins lucrativos de caráter filantrópico, 
            fundada em 24/10/1968, com sede à Rua Professor Aurélio Caciquinho, 500, Januária/MG, CEP: 39480-000. 
            Tendo como idealizadora a Irmã Vera Ribas e a comunidade januarense, com o objetivo de atender crianças e adolescentes carentes, 
            oferecendo-lhes, na medida do possível, melhores condições para atuarem no contexto social em que vivem. O SERVIR oferece diversas 
            oficinas: artes, esporte, informática, reforço escolar, capoeira, karatê, dança, teatro, artesanato e oficina de rádio (projeto jovem locutor), 
            tendo como base a formação humana integral e a inclusão social. O SERVIR oferece alimentação e uma estrutura ampla com salas para oficinas e encontros, 
            bem como refeitório, auditório, quadra esportiva, campo de futebol, área para recreação, uma horta, além de possuir uma rádio comunitária (Servir FM 104,9)
            à serviço da comunidade.</br>
            
            </br>Objetivos da Entidade:</br> 
                • Promover a criança e o adolescente através da educação informal, profissionalização, saúde, lazer, esporte, cultura, carinho e atenção, garantindo-lhes 
                os direitos preconizados no ECA- ( Estatuto da Criança e Adolescente, 8.069/90);</br>
                • Elaborar e executar projetos nas diversas áreas da promoção da assistência social e da promoção da cidadania: educação, saúde, cultura, meio ambiente, 
                promoção e inclusão social, esportes, lazer dentre as demais atividades sociais;</br>
                • Desenvolver o acompanhamento pedagógico por meio do reforço escolar e atividades práticas, capazes de minimizar as dificuldades de ensino/aprendizagem 
                dos alunos;</br>
                • Elaborar e executar projetos e programas de geração de emprego e renda, tanto para os jovens adolescentes quanto para seus familiares, através de oficinas, 
                cursos, seminários e outras atividades que facilitem o acesso e a permanência no emprego;</br>
                • Atuar na defesa, preservação e conservação do meio ambiente e promoção do desenvolvimento sustentável e comunitário;</br>
                • Atuar na área de comunicação social, com o serviço de rádio e TV comunitária, fomentando a divulgação da cultura, do esporte e da ética, favorecendo a 
                democracia e a liberdade de imprensa à comunidade;</br>
                • Atuar na promoção da ética, da paz, dos direitos humanos, da democracia, da justiça social e demais valores universais em defesa da vida.</br>
        
