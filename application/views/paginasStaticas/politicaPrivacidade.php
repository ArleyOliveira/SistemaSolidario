        <p> Nossa política de privacidade visa assegurar a garantia de que, quaisquer informações 
            relativas aos nossos usuários,não serão fornecidas, publicadas ou comercializadas 
            em quaisquer circunstâncias. Tomamos medidas de segurança consistentes  para proteger 
            suas informações pessoais. Essas medidas incluem etapas técnicas e de procedimento para 
            proteger seus dados contra utilização imprópria,acesso ou divulgação não autorizados, perda, 
            alteração ou destruiçã. Essa instituição obtém informações dos usuários de duas maneiras: 
            Cadastro e Cookies. </p> </br>
        
        <h2>Coleta de informações pessoais</h2>
        <p> Toda a coleta de informações pessoais através de nosso site é feita com o total conhecimento e
            consentimento de nossos usuários. Para realizar e/ou manifestar interesse em doação pelo nosso
            site, é necessário o fornecimento de algumas informações do usuário/doador. </p> </br>
            
        <h2>Cadastro</h2>
        <p> Requisitamos informações pessoais quando você faz uma doação, responde a uma pesquisa, participa
            de outras atividades em nosso website. Por exemplo: Se você fizer uma doação on­line, solicitaremos 
            as informações necessárias para completar a doação, tais como seu nome, endereço e telefone. Você é 
            quem decide se deseja prosseguir com qualquer atividade que solicite informações pessoais.
            No entanto, se você não desejar fornecer as informações solicitadas, não será possível completar 
            a transação. </p> </br>
                
        <h2>Cookies e informações de navegação </h2>
        <p> O uso de cookies e informações da navegação (sessão do browser) é feito com o objetivo de traçar
            um perfil anônimo do público que visita o website, buscando aperfeiçoar continuamente nossos serviços.
            Os cookies são identificadores únicos que transferimos para o dispositivo do usuário, que não
            armazenam dados pessoais. Se preferir, o usuário pode apagar os cookies existentes em seu computador 
            através do browser; na maioria dos navegadores, a função "Ajuda" contém instruções sobre como evitar a 
            aceitação de novos cookies, notificação do recebimento de um novo cookie pelo navegador, ou meio para 
            desabilitar todos os cookies de uma só vez. </p></br>
             
        <h2>Compartilhamento de informações</h2>
        <p> Todas as informações fornecidas por nossos doadores são utilizadas exclusivamente para a realização
            da doação e jamais serão cedidas a terceiros. A nossa instuição preza por manter confidencialidade
            das informações fornecida a ela. </p></br>
                    
        <h2> Acesso de informações pelos doadores</h2>
        <p> Os doadores podem acessar informações atualizadas relativas as doações realizada por ele informações
            pessoais.  </p></br>
       
        <h2> Utilização dos dados</h2>
        <p> São dados necessários para a realização da doação: o nome completo da pessoa física ou jurídica;
            email e senha, os quais o doadores preencherá no momento do cadastro para garantir maior segurança;
            endereço, com o objetivo de funcionários da intituição poder ir arrecadar a doação; telefone,
            para que possamos entrar em contato, se houver necessidade. </p></br>
