<br/><h1>Como Doar</h1>
<p>Cada ação em favor do próximo é extremamente importante.
Participar de uma campanha de doação na sua cidade ou região não é apenas alimentar o próximo que não tem recursos para comprar alimento.
É também dizer que, para você, o mundo pode ser um lugar melhor para todos.
Toda doação é um gesto de solidariedade. Doar sangue, tempo, dinheiro ou o que quer que seja é uma atitude generosa, que o universo há de recompensar.
</p>
<h1>Tipos de Doações</h1>
<ul> 
        <li>Doação de alimentos</li> 
        Podem ser doados alimentos não perecíveis dentro da data de validade, mesmo que próxima, e embalagem íntegra
        <li>Doação de Roupas</li> 
        Podem ser doadas roupas novas e usadas, desde que em bom estado de conservação. Doações de cobertores também são muito bem vindas.
        <li>Doação de Móveis</li>
        Podem ser doados móveis novos e/ou usados em bom estado de conservação 
        <li>Doação de Tempo</li>
        ocê pode ajudar doando seu tempo, talentos e conhecimentos. 
        <li>Outros</li>
        Sapatos, produtos de higiene, brinquedos, livros.
    </ul>
