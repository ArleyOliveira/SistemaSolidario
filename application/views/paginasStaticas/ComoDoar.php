        <p> Cada ação em favor do próximo é extremamente importante. Participar de uma campanha 
            de doação na sua cidade ou região não é apenas alimentar o próximo que não tem recursos 
            para comprar alimento. É também dizer que, para você, o mundo pode ser um lugar melhor 
            para todos. Toda doação é um gesto de solidariedade. Doar sangue, tempo, dinheiro ou o 
            que quer que seja é uma atitude generosa, que o universo há de recompensar.</br>
        </p>
        <h3>Tipos de Doações</h3>
        <p>
            • <b>Doação de alimentos:</b> Podem ser doados alimentos não perecíveis dentro da data de validade, 
            mesmo que próxima, e embalagem íntegra;</br>
            
            • <b>Doação de Roupas:</b> Podem ser doadas roupas novas e usadas, desde que em bom estado de conservação. Doações de cobertores também
            são muito bem vindas.</br>
            
            • <b> Doação de Móveis:</b> Podem ser doados móveis novos e/ou usados em bom estado de conservação.</br>
            
            • <b> Doação de Tempo:</b> Você pode ajudar doando seu tempo, talentos e conhecimentos.</br>
            
            • <b> Outros:</b> Sapatos, produtos de higiene, brinquedos, livros.</br>
        </p>
        
